        //MARK: - Константы и Переменные

// Переменная (var) — значение можно менять сколько угодно раз.
var name = "Name"

// Константа (let) — значение задаётся один раз и больше не меняется.
let name2 = "Name2"

// Рекомендуемый подход: сначала let, а когда реально нужно изменение — заменяешь на var.
 
        //MARK: - Строки и многострочные строки

//Строки — это тип String, который хранит Unicode-совместимый текст.
let greeting = "Привет, мир!"

//Многострочные строки - спользуются три двойные кавычки """. Включают все строки между открывающими и закрывающими кавычками.
let multiline = """
Это многострочная строка.
Она сохраняет переносы строк.
"""

        //MARK: - Числа и Числа с плавающей точкой

//Чисда тип Int — размер зависит от архитектуры (32/64 бита). Есть знаковые (Int) и беззнаковые (UInt).
let num = 10

//Литералы в разных системах счисления:
let number = 42         // десятичная
let binary = 0b101010   // двоичная
let octal = 0o52        // восьмеричная
let hex = 0x2A          // шестнадцатеричная

//Double — 64 бита, ~15 знаков точности (по умолчанию для дробных литералов).
let pi: Double = 3.14159265359   // Double по умолчанию
//Float — 32 бита, ~6 знаков точности (экономит память, но менее точен).
let smallPi: Float = 3.14159     // явно Float

        //MARK: - Анотация типов

//Аннотация типов в Swift — это явное указание типа данных для переменной, константы или параметра с помощью двоеточия :
let userName: String = ""
let num1: Int = 10

        //MARK: - Логические операторы

// НЕ (!a) — инвертирует значение: !true = false
// И (a && b) — true, только если оба условия true
// ИЛИ (a || b) — true, если хотя бы одно условие true

//Таблица истинности
//| A     | B     | !A    | A && B | A || B |
//|-------|-------|-------|--------|--------|
//| true  | true  | false | true   | true   |
//| true  | false | false | false  | true   |
//| false | true  | true  | false  | true   |
//| false | false | true  | false  | false  |

// Тернарный условный оператор
//let result = условие ? выражение1 : выражение2
//Логика: если условие true → возвращает выражение1, иначе выражение2.

//Интерполяция строк в Swift — это способ вставить значения переменных или результаты вычислений прямо в строку через \(...).
var переменная = 1
var выражение = 2
let result = "Текст \(переменная) ещё текст \(выражение)"
//Всё внутри \(...) вычисляется, преобразуется в строку и вставляется.

















